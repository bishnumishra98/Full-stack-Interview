// Q) What is CDN ?
// A) CDN stands for Content Delivery Network. It is a network of geographically distributed servers that work
//    together to deliver web content (such as HTML, CSS, JavaScript files, images, videos, and more) to users
//    more quickly and efficiently. CDNs are designed to reduce latency and improve the performance and reliability
//    of web applications by caching content on multiple servers close to the user's location.

// Q) What are the benefits of using a CDN ?
// A) Some of the benefits of using a CDN are:
//    1. Faster Load Times: CDNs cache content on servers located closer to users, reducing latency and improving
//       load times.
//    2. Scalability: CDNs can handle large volumes of traffic and distribute it across multiple servers to prevent
//       overloading.
//    3. Reliability: CDNs improve the reliability of web applications by distributing content across multiple servers
//       and automatically routing traffic to the nearest available server in case of server failures.
//    4. Security: CDNs provide security features like DDoS protection, SSL/TLS encryption, and web application firewalls
//       to protect against cyber attacks and data breaches.
//    5. Cost-Effectiveness: CDNs help reduce bandwidth costs by offloading traffic from origin servers and optimizing
//       content delivery.
//    6. SEO Benefits: CDNs can improve search engine rankings by improving website performance and load times, which are
//       factors considered by search engines like Google when ranking websites.
